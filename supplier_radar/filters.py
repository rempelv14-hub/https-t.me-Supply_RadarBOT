INTENT_WORDS = CLIENT_INTENT_WORDS
TOPIC_WORDS = SEARCH_TAGS + RELATED_TOPIC_WORDS


@dataclass
class LeadScore:
    score: int
    is_valid: bool
    reasons: list[str]


def _matches(text: str, phrases: list[str]) -> list[str]:
    return [phrase for phrase in phrases if phrase in text]


def is_hard_spam(text: str) -> bool:
    text = (text or "").lower()
    return bool(_matches(text, HARD_SPAM_PHRASES))


def score_message(text: str) -> LeadScore:
    original = text or ""
    lowered = original.lower()
    reasons: list[str] = []

    hard_spam = _matches(lowered, HARD_SPAM_PHRASES)
    if hard_spam:
        return LeadScore(
            score=-100,
            is_valid=False,
            reasons=[f"жесткий спам: {', '.join(hard_spam[:5])}"],
        )

    intent_matches = _matches(lowered, CLIENT_INTENT_WORDS)
    tag_matches = _matches(lowered, SEARCH_TAGS)
    related_matches = _matches(lowered, RELATED_TOPIC_WORDS)
    geo_matches = _matches(lowered, GEO_WORDS)
    negative_matches = _matches(lowered, NEGATIVE_WORDS)

    score = 0

    for phrase in intent_matches:
        score += 4
        reasons.append(f"+4 клиентский запрос: {phrase}")

    for phrase in tag_matches:
        score += 5
        reasons.append(f"+5 нужный тег: {phrase}")

    for phrase in related_matches:
        score += 2
        reasons.append(f"+2 похожая тема: {phrase}")

    for phrase in geo_matches:
        score += 1
        reasons.append(f"+1 гео: {phrase}")

    for phrase in negative_matches:
        score -= 6
        reasons.append(f"-6 похоже на продавца/рекламу: {phrase}")

    # Если человек написал короткий вопрос с нужным тегом, например "база поставщиков?",
    # засчитываем это как слабое намерение клиента.
    has_question = "?" in lowered or "подскаж" in lowered
    if has_question and tag_matches:
        score += 3
        reasons.append("+3 вопрос по нужному тегу")

    # Главное правило: нам нужны клиенты.
    # Поэтому без намерения/вопроса и без нужного тега сообщение не отправляем.
    has_client_intent = bool(intent_matches) or (has_question and bool(tag_matches))
    has_required_tag = bool(tag_matches)

    if not has_client_intent:
        reasons.append("нет клиентского намерения")

    if not has_required_tag:
        reasons.append("нет одного из нужных тегов")

    is_valid = (
        has_client_intent
        and has_required_tag
        and not negative_matches
        and score >= settings.min_score
    )

    return LeadScore(score=score, is_valid=is_valid, reasons=reasons)


def is_valid_group(title: str, username: str | None = None, about: str | None = None) -> bool:
    text = f"{title or ''} {username or ''} {about or ''}".lower()

    if any(bad in text for bad in GROUP_NEGATIVE_WORDS):
        return False

    if not settings.only_valid_groups:
        return True

    return any(good in text for good in GROUP_POSITIVE_WORDS)
